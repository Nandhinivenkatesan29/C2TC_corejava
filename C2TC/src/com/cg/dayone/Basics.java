package com.cg.dayone;

public class Basics {

	public static void main(String[] args) {
		int a=10;
		int b=20;
		int sum=a+b;
		int summ=a-b;
		int c=++a;
		int d=--a;
		System.out.println("sum value:"+sum);
		System.out.println("sub value:"+summ);
		System.out.println("incre value:"+c);
		System.out.println("incre value:"+d);
	}

}
